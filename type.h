#ifndef DATA_TYPES_H
#define DATA_TYPES_H

/* 
 * Standard data type definitions for portability and readability.
 * These typedefs ensure consistent type sizes across platforms.
 */

typedef unsigned int    uint32_t; /* 32-bit unsigned integer */
typedef int             int32_t;  /* 32-bit signed integer */
typedef unsigned short  uint16_t; /* 16-bit unsigned integer */
typedef short           int16_t;  /* 16-bit signed integer */
typedef unsigned char   uint8_t;  /* 8-bit unsigned integer */
typedef char            int8_t;   /* 8-bit signed integer */

typedef unsigned long   size_t;   /* Size type, typically used for memory sizes */

#endif /* DATA_TYPES_H */
